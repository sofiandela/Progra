public class OperadoresDeAsignacion{
	public static void main (String args[]){
		int a = 0 ;
		int b = 0;
		int c = 0;
		int d = 0;
	
        a = 1;
        b = 1;

        c = a++;

        System.out.println(a);
        System.out.println(c);

        d = ++b;

        System.out.println(b);
        System.out.println(d);
    }
}

